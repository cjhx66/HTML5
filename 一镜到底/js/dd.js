function init(){
    netease.autoPlay("bgm");
    $(".loading-wrap").addClass("g");
    $(".loading-wrap.g .loading-fei")[0].addEventListener("webkitAnimationEnd", function(){ //动画结束时事件
        $(".loading-wrap").fadeOut(500);
    }, false);
    $(".share-box").on("click",function(){
        $(".share-box").fadeOut(400);
    });
    $(".music").on("click",function(){
        if($("#bgm")[0].paused){
            // 播放
            $("#bgm")[0].play();
            $(".music").removeClass("off").addClass("on");
        }else{
            // 暂停
            $("#bgm")[0].pause();
            $(".music").removeClass("on").addClass("off");
        }
    });
    $(".main").on("touchstart",function(){
        $(".long-tap").removeClass("ani");
    });
}

